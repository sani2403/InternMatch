                    <h4 class="title">About Scholarship</h4>
                    <div class="content">
                        Kotak Kanya Scholarship is a collaborative CSR Project of Kotak Mahindra Group companies and Kotak Education Foundation to promote education and livelihood among economically disadvantaged sections of society. This scholarship aims to offer financial assistance to meritorious girl students from low-income families to empower them to pursue higher studies in professional education after Class 12.
                        <br>
                        Under Kotak Kanya Scholarship 2024-25, girl students who have passed Class 12 and aspire to pursue professional graduation courses like Engineering, MBBS, BDS, Integrated LLB (5 Years), B. Pharmacy, B.Sc. Nursing, Integrated BS-MS/BS-Research, in ISER, IISC (Bangalore), or other professional courses (Design, Architecture, etc.) from institutes of repute (NAAC/NIRF accredited) will be provided scholarship of INR 1.5 lakh* per year to pay their educational expenses until graduation (degree).
                        <br>
                    </div>

                    <h4 class="title">Eligibility</h4>
                    <div class="content">
                        Open for meritorious girl students across India.
                        <br>
                        Applicants must have scored 75% or more marks or equivalent CGPA in Class 12 board examinations.
                        <br>
                        Applicant's annual family income must be less than INR 6,00,000.
                        <br>
                        Meritorious girl students who have secured admission to the first year of graduation programs in the academic year 2024-25 in institutes of repute like NIRF/NAAC accredited for professional graduation degrees like Engineering, MBBS, BDS, Integrated LLB (5 Years), B.Sc. Nursing, B. Pharmacy, Integrated BS-MS/BS-Research in ISER, IISC (Bangalore), or other professional courses (Design, Architecture, etc.).
                        <br>
                        Children of employees of Kotak Mahindra Group, Kotak Education Foundation are not eligible to apply for the Kotak Kanya Scholarship 2024-25.
                        <br>
